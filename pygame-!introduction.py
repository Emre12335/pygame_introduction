# Pygame ne yapar?

# pygame bizim ekranda resim çizmemizi sağlar ve o resimleri kullanıcıya göndermemizi sağlar.
# yani pygame bir oyun kurma amaçlı yapılsa bile aslında bir film bile oynatabilirsin.
# resim çizmek zor olduğu için pygame sayesnde resimleri oynatabiliriz.
# pygame kullanıcı inputları için elverişlidir. Ve inputları sürekli kontrol eder.
# python dahazır input alıcı varken niye pygame ise input() fonksiyonu oyunun durmasına yol açar ve reaksiyonu azaltır.Bu yüzden pygame tercih edilir.


